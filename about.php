     <div class="bio">
         <h3>School Owner Bio</h3>

             <p>Master Donald Green (Moy Don) is a Master at Wing Chun (Ving Tsun) Kung-Fu. He started his journey in the
             martial arts at the age of six. For three years, he learned several Southern Chinese martial arts styles, mainly Southern
             Shaolin Long Fist and Chen Style Tai Chi. At age nine, under the tutelage of Master Ting, he took Tiger Claw Style and the
             closed-door family system Black Tiger Claw until he left for the Navy at age 17. Also during that same time, he
             would train in several other forms of combat. Green trained Tiger Claw on the weekends and other forms of Martial
             Arts such as Chito Ryu Karate, Ju Jit Su, Judo, Greco Roman and Free Style Wrestling, Boxing, and Kick Boxing on other days.
             During this time, Master Green competed in many point tournaments, winning several, and also started doing full-contact 
			 no rules fighting. He also took several seminars in Wing Chun, but could not find a teacher (Sifu) close
             enough to take lessons from.</p> 

	     <p>When Master Green arrived in his first duty station in the Navy, he was able to take
             formal lessons from Master Duncan Leung in Virginia Beach, Virginia. Along with a friend from his team, Hector Cortez,
             he learned Wing Chun and they trained while deployed. He also trained in Kajukembo, in which Hector was a
             Black Belt. He also was able during his Navy years to train in Mongolian Wrestling in Italy, Muay Thai in
             Thailand, and Krav Maga in Israel. After leaving the military, he returned to Rochester, New York. Green was in search of a life
             long teacher to finish his training in Wing Chun, the martial art had fully captivated him.</p>


             <p>He took a job teaching in a local Wing Chun club, but traveled to New York City to start training with Grand
             Master Moy Yat. During this time, he would travel to the city on weekends to train and work in security and body
             guarding during the week. He worked with clients such as Lou Gramm and “Down Town” Julie Brown. This took its
             toll on Master Green, and he took Grandmaster Moy Yat’s advice to train with Yat's student, now Grandmaster, Dunn
             Wah (Sunny) Tang in Toronto, Canada. Green earned his Gold Sash, or mastery of the system, under Grandmaster Tang in
             1993. He also competed for Team Canada and continued fighting all over the world in the Canadian Championships,
             European Championships, and many other five star ranked tournaments world wide. These included three full-contact Muay Thai
             matches in Thailand, with Green winning all three. He then returned to Grandmaster Moy Yat and became his disciple in a Bise
             ceremony. Green stayed with Grandmaster Moy Yat until Yat's death in 2001 and was able to achieve mastery of the system.</p>
	     
	     <p>Greene continued to compete in full contact tournaments world-wide, winning international
             tournaments and competing for both Canada and America before retiring from fighting when his first son was born.
	     He is also a veteran of several real-life challenge matches witnessed by students and friends
             alike. Green also continued to work in bouncing ,and club security, and private bodyguarding. He currently lives in Webster,
             New York with his wife Jennifer, three sons, and two step-daughters. He is now celebrating his 43rd
             year in the martial arts and his 32nd year exclusively in Wing Chun Kung Fu. He has been included in the Action
             Martial Arts Hall of Fame and has received several awards for teaching Martial Arts. He has been teaching in
             Rochester, New York on and off from 1993. He now owns a school (Kwoon) in Rochester, located on 80 Rockwell Place near the
             Record Archives suite 208.</p>
         </p>
