         <h3>School Owner Bio</h3>

         <p>
             Master Donald Green (aka Moy Don) is a Master at Wing Chun (Ving Tsun) Kung-Fu. He started his journey in the
             Martial Arts at the age of 6 yrs. old for three years he learned several Southern Chinese styles mainly southern
             Shaolin Long Fist and Chen Style Tai Chi. At 9 under the tutelage of Master Ting he took Tiger Claw Style and a
             closed door family system Black Tiger Claw until he left for the Navy at age 17. Also during that same time he
             would train in several other forms of combat. Training in Tiger Claw on the weekends and other forms of Martial
             Arts such as Chito Ryu Karate , Ju Jit Su, Judo, Greco Roman and Free Style Wrestling, Boxing, and Kick Boxing.
             During this time also Master Green competed in many point tournaments winning several and also started in full
             contact no rules fighting. He also took several seminars in Wing Chun  but could not find a teacher (Sifu) close
             enough to take lessons from. When Master Green arrived in his first duty station in the Navy he was able to take
             formal lessons from Master Duncan Leung in Virginia  Beach, Va. along with a friend from his team Hector Cortez
             they learned Wing Chun together and trained while deployed. He also trained in Kajukembo which Hector was a
             Black Belt. He also was able during his Navy years to train in Mongolian Wrestling in Italy, Muay Thai in
             Thailand, and Krav Maga in Israel. After leaving the military he returned to Rochester, N.Y. In search of a life
             long teacher to finish his training in Wing Chun the Martial Art that would finally take his full interest and
             loyalty. He took a job teaching in a local Wing Chun club but traveled to N.Y.C. to start training with Grand
             Master Moy Yat. During this time he would travel to the city on weekends to train and work in security and body
             guarding during the week. With clients such a Lou Gramm and “Down Town” Julie Brown to name a few. This took its
             toll on Master Green and he took Grandmaster Moy Yat’s advice and trained with his student now Grandmaster Dunn
             Wah (Sunny) Tang in Toronto, Canada. He earned his Gold Sash or mastery of the system under Grandmaster Tang in
             1993. He also competed for team Canada and continued fighting all over the world Canadian Championships,
             European Championships and many five star ranked tournaments world wide including three full contact Muay Thai
             matches in Thailand winning all three. He then returned to Grandmaster Moy Yat and became his disciple in a Bise
             ceremony. He stayed with Grandmaster Moy Yat until his death and achieved mastery of the system through him
             before his death in 2001. He continued to compete in full contact tournaments world wide winning world
             tournaments and competing for both Canada and The USA before retiring from fighting when his first son was born
             at the age of 34. He is also a veteran of several real life challenge matches witnessed by students and friends
             alike. He also continued to work in Bouncing and Club Security and private bodyguarding. He lives in Webster
             New York with his new wife Jennifer and his three boys and two step daughters. He now is celebrating his 43rd
             year in the Martial Arts and his 32nd year exclusively in Wing Chun Kung Fu. He has been included in the Action
             Martial Arts Hall of Fame and received several rewards for teaching Martial Arts. He has been teaching in
             Rochester, N.Y. on and off from 1993. He now owns a school (Kwoon) in Rochester on 80 Rockwell Place near the
             Record Archives suite 208.
         </p><br>

